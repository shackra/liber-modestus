def book_name(book: Book, locale: str = "la") -> str:
    """Return the localised name for *book* in the given *locale*.

    Parameters
    ----------
    book:
        The book to name.
    locale:
        An IANA locale identifier, e.g. ``"la"``, ``"es"``, ``"en"``,
        ``"pl"``, ``"cs"``, ``"da"``, ``"de"``, ``"fr"``, ``"it"``,
        ``"hu"``, ``"nl"``, ``"pt"``, ``"uk"``, ``"vi"``, ``"ja"``,
        ``"ko"``, ``"zh"``.  Falls back to the Latin canonical name
        if the locale is not available.
    """
    abbr = _get_latin_abbr(book)
    locale_data = _LOCALE_DATA.get(locale) or {}
    return locale_data.get(abbr) or _get_latin_name(book)


def _get_latin_abbr(book: Book) -> str:
    """Get canonical Latin abbreviation for a book."""
    from .canon import _BOOK_META

    return _BOOK_META[book].latin_abbr


def _get_latin_name(book: Book) -> str:
    """Get Latin name for a book."""
    from .canon import _BOOK_META

    return _BOOK_META[book].latin_name
